package com.JavaCollege;

public class doingOperationsWithDataTypes {

    public static void main(String[] args) {

        byte a = 90;
        char c = 'z';
        int b = 34;
        float d = 45.34f;
        double e = 90.32d;
        short f = 1024;

        System.out.println(c / b);
        System.out.println(f * e);
        System.out.println(c / a);

    }
}
