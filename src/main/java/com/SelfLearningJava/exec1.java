package com.SelfLearningJava;

public class exec1 {
    public static void main(String[] args) {
        double d = 1.5, D = 2.5;

        System.out.println(d++ + " "+ ++D);

        char c = 'd' , e = 'D';

        System.out.println(c++ + ""+ ++e);

        char g = 'd' , f = 'D';

        System.out.println(g++ * ++f);

        char x = 'a';
        int y = (int) x;

        System.out.println(y);


        char ch = 'A';

        int ASCII = ch;
        System.out.println(ASCII);

        //System.out.println(d * E);
    }
}
