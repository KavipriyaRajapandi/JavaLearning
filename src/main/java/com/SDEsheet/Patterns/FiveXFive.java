package com.SDEsheet.Patterns;

public class FiveXFive {
    public static void main(String[] args) {
        int n = 5;

        for(int i=0;i<n;i++){
            for (int j = 0; j < n; j++) {
                if(true) {
                    System.out.print("* ");
                }
            }
            System.out.println();
        }
    }
}
