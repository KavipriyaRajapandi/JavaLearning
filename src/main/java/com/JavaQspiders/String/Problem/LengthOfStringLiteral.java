package com.JavaQspiders.String.Problem;

public class LengthOfStringLiteral {
    public static void main(String[] args) {
        String a = "Qspiders";
        String b = "Jspiders";
        String name = "SibiVarshaan";
        int ans = a.length();
        int ans2 = b.length();
        System.out.println("Qspiders:"+ans);
        System.out.println("Jspiders:"+ans2);
        System.out.println("SibiVarshaan:"+name.length());
    }
}
